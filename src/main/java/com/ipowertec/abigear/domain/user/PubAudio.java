package com.ipowertec.abigear.domain.user;

public class PubAudio {
	
	private String res_duration;
	private String created_at;
	private String res_name;
	private String res_file;
	private String res_md5;
	private String id;
	
	public String getRes_duration() {
		return res_duration;
	}
	public void setRes_duration(String res_duration) {
		this.res_duration = res_duration;
	}
	public String getCreated_at() {
		return created_at;
	}
	public void setCreated_at(String created_at) {
		this.created_at = created_at;
	}
	public String getRes_name() {
		return res_name;
	}
	public void setRes_name(String res_name) {
		this.res_name = res_name;
	}
	public String getRes_file() {
		return res_file;
	}
	public void setRes_file(String res_file) {
		this.res_file = res_file;
	}
	public String getRes_md5() {
		return res_md5;
	}
	public void setRes_md5(String res_md5) {
		this.res_md5 = res_md5;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
}
